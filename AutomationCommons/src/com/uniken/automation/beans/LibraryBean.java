package com.uniken.automation.beans;

public class LibraryBean{
	private int lib_id;
	private String lib_name;
	private String lib_version;
	private String lib_type;
	public int getLib_id() {
		return lib_id;
	}
	public void setLib_id(int lib_id) {
		this.lib_id = lib_id;
	}
	public String getLib_name() {
		return lib_name;
	}
	public void setLib_name(String lib_name) {
		this.lib_name = lib_name;
	}
	public String getLib_version() {
		return lib_version;
	}
	public void setLib_version(String lib_version) {
		this.lib_version = lib_version;
	}
	public String getLib_type() {
		return lib_type;
	}
	public void setLib_type(String lib_type) {
		this.lib_type = lib_type;
	}
		
}