package com.uniken.automation.beans;

public class FeatureBean {
	private int feature_id;
	private String feature_name;
	private String feature_target;
	public int getFeature_id() {
		return feature_id;
	}
	public void setFeature_id(int feature_id) {
		this.feature_id = feature_id;
	}
	public String getFeature_name() {
		return feature_name;
	}
	public void setFeature_name(String feature_name) {
		this.feature_name = feature_name;
	}
	public String getFeature_target() {
		return feature_target;
	}
	public void setFeature_target(String feature_target) {
		this.feature_target = feature_target;
	}
	
}